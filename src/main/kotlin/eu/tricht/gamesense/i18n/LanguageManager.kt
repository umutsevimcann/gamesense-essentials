            "clock_24h" to "24-hour format",
            "show_time" to "Show time"
        )
    }
    
    fun translate(key: String): String {
        val langMap = translations[currentLanguage] ?: translations["en"] ?: mapOf()
        return langMap[key] ?: translations["en"]?.get(key) ?: key
    }
    
    fun setLanguage(lang: String) {
        if (supportedLanguages.containsKey(lang)) {
            currentLanguage = lang
        }
    }
